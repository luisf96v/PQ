    @Override
    public void actionPerformed(ActionEvent e) {
        String text = ((JButton) e.getSource()).getText();
        JPanel p = null;

        switch (text) {
            case "Entrada": {
                p = new Registrar();
                break;
            }
            case "Salida": {
                p = new Salida();
                break;
            }
            case "Lista Vehiculos": {
                p = new TablaV();
                break;
            }
            case "Ingresos": {
                p = new Ingresos();
                break;
            }
            default:
                return;
        }

        this.jPanel1.removeAll();
        javax.swing.JPanel j = new Registrar();
        p.setBounds(0, 0, 700, 510);
        this.jPanel1.add(p);
        this.jButton2.setVisible(true);
        this.validate();
        this.repaint();
    }

