    private void datosTabla() {
        ArrayList<Vehiculo> list = (ArrayList<Vehiculo>) Fichero.buscaLista();
        Iterator<Vehiculo> itr = list.iterator();
        int row = 0;
        int c = 0, m = 0, o = 0;

        while (itr.hasNext()) {
            Vehiculo aux = itr.next();
            this.jTable1.setValueAt(aux.getEntrada().toString("HH : mm"), row, 0);
            this.jTable1.setValueAt(aux.getTipo(), row, 1);
            switch (aux.getTipo()) {
                case "Autom칩vil Act": case "Autom칩vil":{
                    c++;
                    break;
                }
                case "Motocicleta Act": {
                    m++;
                    break;
                }
                case "Otro": {
                    o++;
                    break;
                }
            }
            this.jTable1.setValueAt(aux.getMatricula(), row, 2);
            this.jTable1.setValueAt(aux.getNumFac(), row++, 3);
        }
        this.o.setText(String.valueOf(o));
        this.c.setText(String.valueOf(c));
        this.m.setText(String.valueOf(m));
        
        
    }

